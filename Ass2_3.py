#Bitwise Operators:
a=10
b=2

result = a & b  
print("Bitwise AND:", result)

result = a | b  
print("Bitwise OR:", result)

result = a ^ b  
print("Bitwise XOR:", result)

result = ~b  
print("Bitwise NOT:", result)

result = a << 1  
print("Left shift:", result)

result = a >> 1 
print("Right shift:", result)


